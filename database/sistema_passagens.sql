CREATE DEFINER=`root`@`localhost` PROCEDURE `gerar_voos_proximos_dias` (IN `dias_a_frente` INT)   BEGIN
    DECLARE done INT DEFAULT FALSE;
    DECLARE r_origem   VARCHAR(100);
    DECLARE r_destino  VARCHAR(100);
    DECLARE r_preco    DECIMAL(10,2);
    DECLARE r_assentos INT;
    DECLARE d INT DEFAULT 0;
    DECLARE data_voo DATE;

    DECLARE cur CURSOR FOR
        SELECT origem, destino, preco, assentos_padrao FROM rota;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = TRUE;

    OPEN cur;
    loop_rotas: LOOP
        FETCH cur INTO r_origem, r_destino, r_preco, r_assentos;
        IF done THEN LEAVE loop_rotas; END IF;

        SET d = 0;
        WHILE d <= dias_a_frente DO
            SET data_voo = DATE_ADD(CURDATE(), INTERVAL d DAY);

            IF NOT EXISTS (
                SELECT 1 FROM voo
                WHERE origem = r_origem AND destino = r_destino AND data = data_voo
            ) THEN
                INSERT INTO voo (origem, destino, data, preco, assentos_disponiveis)
                VALUES (r_origem, r_destino, data_voo, r_preco, r_assentos);
            END IF;

            SET d = d + 1;
        END WHILE;

        SET done = FALSE;
    END LOOP;

    CLOSE cur;
END$$

